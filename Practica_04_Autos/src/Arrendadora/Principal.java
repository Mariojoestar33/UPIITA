    private void agregarRenta(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarRenta
        // TODO add your handling code here:
        
        try {
            
            Renta prestado;
        
                prestado = new Renta();

                int seguro = cbSeguro.getSelectedIndex();

                int selecciocarro = lCarrosRenta.getSelectedIndex();

                if(listadeAutos[selecciocarro].getOcupado() != 1) {

                        int seleccioncliente = lClientesRenta.getSelectedIndex();

                        String fechai = txtfechaInicio.getText();
                        String fechaf = txtFechaFin.getText();

                        String[] separacionesi = fechai.split("/");
                        String[] separacionesf = fechaf.split("/");

                        int diai = Integer.parseInt(separacionesi[0]); 
                        int mesi = Integer.parseInt(separacionesi[1]); 
                        int anioi = Integer.parseInt(separacionesi[2]); 

                        int diaf = Integer.parseInt(separacionesf[0]); 
                        int mesf = Integer.parseInt(separacionesf[1]); 
                        int aniof = Integer.parseInt(separacionesf[2]); 

                        Calendar fechainicio = Calendar.getInstance();
                        fechainicio.set(anioi, mesi - 1, diai);

                        Calendar fechafinal = Calendar.getInstance();
                        fechafinal.set(aniof, mesf - 1, diaf);

                        //Inicio de inserciones de datos

                        prestado.setFechainicial(fechainicio);
                        prestado.setFechafinal(fechafinal);

                        prestado.setConSeguro(seguro);

                        prestado.setCarcacha(listadeAutos[selecciocarro]);

                        prestado.setVictima(listadeClientes[seleccioncliente]);

                        int total = prestado.calcularRenta();

                        modeloListaRentas.addElement(prestado.getCarcacha().getPlaca());

                        //txtTotal.setText(String.valueOf(total));

                        listadeRenta[numRentasRegistradas] = prestado;

                        numRentasRegistradas++;

                        listadeAutos[selecciocarro].setOcupado(1);

                        prestado.setEstado(1);

                        limpiarCampos();
            
            } else {
            
            JOptionPane.showMessageDialog(null, "El carro esta ocupado, elija otro!!!");
            limpiarCampos();
            
            }
        
            
        } catch(Exception e) {
            
            JOptionPane.showMessageDialog(null, "Ingrese los datos correctamente!!!");
            
        }
        
    }//GEN-LAST:event_agregarRenta

