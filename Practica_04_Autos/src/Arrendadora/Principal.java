    private void seleccionRenta(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_seleccionRenta
        // TODO add your handling code here:
        
        try {
            
           int seleccion = lRentas.getSelectedIndex();
        txtclienteRenta.setText(listadeRenta[seleccion].getVictima().getNombre());
        txtcarroRenta.setText(listadeRenta[seleccion].getCarcacha().getMarca());
        txtfechaInicio.setText(String.valueOf(listadeRenta[seleccion].getFechainicial().getTime()));
        txtFechaFin.setText(String.valueOf(listadeRenta[seleccion].getFechafinal().getTime()));
        txtTotal.setText("$"+String.valueOf(listadeRenta[seleccion].getTotal()));
        cbSeguro.setSelectedIndex(listadeRenta[seleccion].getConSeguro());
        
        if(listadeRenta[seleccion].getEstado() != 1) {
            
            txtEstado.setText("LIBERADO");
            
        } else {
            
            txtEstado.setText("CON EL CLIENTE");
            
        } 
            
        } catch(Exception e) {
            
            JOptionPane.showMessageDialog(null, "Todavia no hay rentas en el registro");
            
        }
        
        
        
    }//GEN-LAST:event_seleccionRenta

